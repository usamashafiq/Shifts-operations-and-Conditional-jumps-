Code 1:
.MODEL SMALL
.STACK 100H
.DATA
MSG DB "ENTER A CHARACTER : $"
MSG1 DB 0AH,0DH,"BINARY VALUE IS : $"
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV DX,OFFSET MSG
MOV AH,09H
INT 21H
MOV AH,01
INT 21H
MOV BL,AL
MOV DX,OFFSET MSG1
MOV AH,09H
INT 21H
MOV CX,8
BINARY:
SHR BL,1
JNC CF
MOV DL,'1'
JMP DISPLAY
CF:
MOV DL,'0'
DISPLAY:
MOV AH,02
INT 21H
LOOP BINARY
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
Code 2:
.MODEL SMALL
.STACK 100H
.DATA
A DB "ENTER YOUR PASSWORD: $"
OK DB "CORRECT PASSWORD $"
NOTOK DB "PASSWORD IS WRONG $"
PASSWORD DB "DATASTRU $"
PASSWORD1 DW 8
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV CX,PASSWORD1
MOV BX,OFFSET PASSWORD
MOV AH,9
LEA DX,A
INT 21H
L1:
MOV AH,8
INT 21H
CMP AL,[BX]
JNE L2
INC BX
LOOP L1
MOV AH,9
LEA DX,OK
INT 21H
JMP EXIT
L2:
MOV AH,9
LEA DX,NOTOK
INT 21H
EXIT:
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
